CREATE TABLE hr.EDUCATION (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
NAME_OF_UNIVERSITY VARCHAR2(30) NOT NULL,
TYPE_OF_EDUCATION VARCHAR2(30) NOT NULL,
DATE_OF_START DATE NOT NULL,
DATE_OF_FINISH DATE NOT NULL,
PRIMARY KEY(ID));

CREATE TABLE hr.EXPIRIENCE (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
JOB_TITLE VARCHAR2(30) NOT NULL,
DATE_OF_START DATE NOT NULL,
DATE_OF_FINISH DATE NOT NULL,
PRIMARY KEY(ID));

CREATE TABLE hr.PERSON_TO_CONTACT (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
NAME_OF_PERSON VARCHAR2(30) NOT NULL,
PHONE VARCHAR2(30) NOT NULL,
ADRESS VARCHAR2(30) NOT NULL,
PRIMARY KEY(ID));

CREATE TABLE hr.EMPLOYER_HISTORY (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
JOBS_POSTED NUMBER(10) NOT NULL,
JOBS_REJECTED NUMBER(10) NOT NULL,
JOBS_APPROVED NUMBER(10) NOT NULL,
JOBS_IN_PROGERSS NUMBER(10) NOT NULL,
PRIMARY KEY(ID));

CREATE TABLE hr.EMPLOYERS (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
NAME_OF_COMPANY VARCHAR2(30) NOT NULL,
PERSON_TO_CONTACT NUMBER(10) NOT NULL,
JOBS_HISTORY NUMBER(10) NOT NULL, 
PRIMARY KEY(ID),
FOREIGN KEY (PERSON_TO_CONTACT) REFERENCES PERSON_TO_CONTACT(ID),
FOREIGN KEY (JOBS_HISTORY) REFERENCES EMPLOYER_HISTORY(ID));

CREATE TABLE hr.VACANCYS (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
EMP_ID NUMBER(10) NOT NULL,
NAME_OF_VACANCY VARCHAR2(30) NOT NULL,
VACANCY_POSITION VARCHAR2(30) NOT NULL,
COMPANY VARCHAR2(30) NOT NULL,
SALARY NUMBER(10) NOT NULL,
CONTACT_PERSON NUMBER(10) NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY (EMP_ID) REFERENCES EMPLOYERS(ID),
FOREIGN KEY (CONTACT_PERSON) REFERENCES PERSON_TO_CONTACT(ID));

CREATE TABLE hr.EMPLOYEES_HISTORY (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
JOBS_APPLIED NUMBER(10) NOT NULL,
JOBS_REJECTED NUMBER(10) NOT NULL,
JOBS_APPROVED NUMBER(10) NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY (JOBS_APPLIED) REFERENCES VACANCYS(ID));

CREATE TABLE hr.EMPLOYEE_DATA (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
NAME_OF_EMPLOYEE VARCHAR2(30) NOT NULL,
PHONE VARCHAR2(30) NOT NULL,
DATE_OF_BIRTH DATE NOT NULL,
ADRESS VARCHAR2(30) NOT NULL,
CITY VARCHAR2(30) NOT NULL,
EMPLOYEE_HISTORY NUMBER(10) NOT NULL,
VACANCY NUMBER(10) NOT NULL, 
PRIMARY KEY(ID),
FOREIGN KEY (EMPLOYEE_HISTORY) REFERENCES EMPLOYEES_HISTORY(ID),
FOREIGN KEY (VACANCY) REFERENCES VACANCYS(ID));

CREATE TABLE hr.EMPLOYEE (
ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY,
EDUCATION NUMBER(10) NOT NULL,
EXPIRIENCE NUMBER(10) NOT NULL,
PERSON_DATA NUMBER(10) NOT NULL,
HISTORY_OF_PERSON NUMBER(10) NOT NULL,
PRIMARY KEY(ID),
FOREIGN KEY (EDUCATION) REFERENCES EDUCATION(ID),
FOREIGN KEY (EXPIRIENCE) REFERENCES EXPIRIENCE(ID),
FOREIGN KEY (PERSON_DATA) REFERENCES EMPLOYEE_DATA(ID));












